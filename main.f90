PROGRAM MAIN
    USE PARAMETERS
    USE MD
    IMPLICIT NONE
    REAL, DIMENSION(N, D) :: R, V ! POSITION AND VELOCITY VECTORS
    INTEGER :: I

    CALL RANDOM_NUMBER(R)
    CALL RANDOM_NUMBER(V)

    R = R * L
    V = (V * 200) - 100

    PRINT *, "POSITIONS:"
    PRINT *, R
    PRINT *, "VELOCITIES:"
    PRINT *, V
    
    DO I = 0, 100
        PRINT '(A, F10.6)', "POSITION AT T = ", DT * I
        PRINT *, R
        PRINT '(A, F10.6)', "VELOCITY AT T = ", DT * I
        PRINT *, V
        CALL DUMP(R, DT * I, I)
        CALL UPDATE(R, V)
    END DO

END PROGRAM MAIN